/*
 * Fixture: compiled binary that embeds well-known crypto constants in .rodata.
 *
 * Tests YARA-X rules that detect crypto algorithms by their characteristic
 * byte tables (S-boxes, round constants, hash IVs) — the patterns persist
 * even when symbol names are stripped. cradar already has these patterns
 * in `cli/internal/scanner/binary/patterns.go` for the native scanner; the
 * YARA-X rules in Sub-PR B re-express them as YARA rules for uniformity.
 *
 * Constants included:
 *   - AES forward S-box (256 bytes)
 *   - AES round constants (Rcon, first 10 values)
 *   - MD5 initial state (4 x 32-bit, little-endian on the wire)
 *   - SHA-256 initial state (8 x 32-bit hash IV)
 *   - SHA-256 round constants (first 4 of 64 — full table omitted for brevity)
 *
 * Build:  make crypto-constants
 * Scan:   cradar scan dist/crypto-constants --passes 3 --rules-dir <yara-rules>
 */
#include <stdio.h>
#include <stdint.h>

